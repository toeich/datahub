	@Override
	public void enter(ViewChangeEvent event) {
		try {
			long datasetGroupId = Long.parseLong(event.getParameters());
			datasetGroup = datasetService.getDatasetGroup(datasetGroupId);
			
			label.setValue(Messages.format(this, "label_value", datasetGroup.getName()));

			datasetGroupName.setValue(datasetGroup.getName());
			datasetGroupDescription.setValue(StringUtils.abbreviate(datasetGroup.getDescription(), 100));

			aboutText.setValue(datasetGroup.getDescription());
			aboutText.setValue("" + projectService.getProjects(datasetGroup));

			for (Object propertyId : datasetsTable.getVisibleColumns()) {
				datasetsTable.removeGeneratedColumn(propertyId);
			}
			for (final DatasetField field : datasetGroup.getFields()) {
				if (field.isVisible()) {
					datasetsTable.addGeneratedColumn(field.getName(), new ColumnGenerator() {
						@Override
						public Object generateCell(Table source, Object itemId, Object columnId) {
							final Dataset dataset = (Dataset) itemId;
							return dataset.getFields().get(columnId);
						}
					});
					datasetsTable.setColumnExpandRatio(field.getName(), 1.0f);
				}
			}
			datasetsTable.addGeneratedColumn("actions", new ColumnGenerator() {
				@Override
				public Object generateCell(Table source, Object itemId, Object columnId) {
					final Dataset dataset = (Dataset) itemId;
					HorizontalLayout actions = new HorizontalLayout();
					actions.setSpacing(true);
					Button editButton = new Button();
					editButton.setStyleName("link");
					editButton.setCaption(Messages.getString("edit"));
					editButton.addClickListener(new ClickListener() {
						@Override
						public void buttonClick(ClickEvent event) {
							UI.getCurrent().getNavigator().navigateTo(DATASET + dataset.getId());						}
					});
					actions.addComponent(editButton);
					Button deleteButton = new Button();
					deleteButton.setStyleName("link");
					deleteButton.setCaption(Messages.getString("delete"));
					deleteButton.addClickListener(new ClickListener() {
						@Override
						public void buttonClick(ClickEvent event) {
							datasetService.removeDataset(dataset);
							refreshDatasetsTable();
						}
					});
					actions.addComponent(deleteButton);
					return actions;
				}
			});
			
			refreshDatasetsTable();
		} catch (Exception e) {
			Notification.show("Error", e.getMessage(), Notification.Type.ERROR_MESSAGE);
			UI.getCurrent().getNavigator().navigateTo(DATASET_GROUPS);
		}
	}

	private void refreshDatasetsTable() {
		datasets.removeAllItems();
		for (Dataset dataset: datasetService.getDatasets(datasetGroup)) {
			datasets.addItem(dataset);
		}
	}

