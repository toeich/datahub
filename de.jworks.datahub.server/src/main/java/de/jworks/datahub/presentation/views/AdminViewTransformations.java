package de.jworks.datahub.presentation.views;

import java.util.Arrays;

import javax.annotation.PostConstruct;
import javax.inject.Inject;

import com.vaadin.annotations.AutoGenerated;
import com.vaadin.data.util.BeanItemContainer;
import com.vaadin.ui.Button;
import com.vaadin.ui.Button.ClickEvent;
import com.vaadin.ui.Button.ClickListener;
import com.vaadin.ui.CustomComponent;
import com.vaadin.ui.HorizontalLayout;
import com.vaadin.ui.Label;
import com.vaadin.ui.Table;
import com.vaadin.ui.Table.ColumnGenerator;
import com.vaadin.ui.UI;
import com.vaadin.ui.VerticalLayout;
import com.vaadin.ui.Window;
import com.vaadin.ui.Window.CloseEvent;
import com.vaadin.ui.Window.CloseListener;

import de.jworks.datahub.business.transform.boundary.DatasinkService;
import de.jworks.datahub.business.transform.boundary.DatasourceService;
import de.jworks.datahub.business.transform.boundary.TransformationService;
import de.jworks.datahub.business.transform.entity.Transformation;
import de.jworks.datahub.presentation.editors.TransformationEditor;
import de.jworks.datahub.presentation.editors.TransformationEditor.SaveEvent;
import de.jworks.datahub.presentation.editors.TransformationEditor.SaveListener;

public class AdminViewTransformations extends CustomComponent {

	/*- VaadinEditorProperties={"grid":"RegularGrid,20","showGrid":true,"snapToGrid":true,"snapToObject":true,"movingGuides":false,"snappingDistance":10} */

	@AutoGenerated
	private VerticalLayout mainLayout;
	@AutoGenerated
	private HorizontalLayout buttons;
	@AutoGenerated
	private Button newTransformationButton;
	@AutoGenerated
	private Table table;
	@AutoGenerated
	private Label label;
	
	@Inject
	TransformationService transformationService;
	
	@Inject
	DatasinkService datasinkService;
	
	@Inject
	DatasourceService datasourceService;
	
	private BeanItemContainer<Transformation> transformations = new BeanItemContainer<Transformation>(Transformation.class);
	
	public AdminViewTransformations() {
		buildMainLayout();
		setCompositionRoot(mainLayout);
		
		table.setSelectable(true);
		table.setMultiSelect(true);
		
		table.addGeneratedColumn("actions", new ActionsColumn());
		
		table.setContainerDataSource(transformations, Arrays.asList("name", "actions"));
		table.setColumnExpandRatio("name", 1.0f);
		
		newTransformationButton.addClickListener(new NewTransformationHandler());
	}

	@PostConstruct
	public void refreshTable() {
		transformations.removeAllItems();
		transformations.addAll(transformationService.getTransformations());
	}

	private class ActionsColumn implements ColumnGenerator {
		
		@Override
		public Object generateCell(Table source, Object itemId, Object columnId) {
			Transformation transformation = transformations.getItem(itemId).getBean();
			HorizontalLayout actions = new HorizontalLayout();
			actions.setSpacing(true);
			actions.addComponent(new EditTransformationButton(transformation));
			actions.addComponent(new DeleteTransformationButton(transformation));
			return actions;
		}
	}

	private class EditTransformationButton extends Button implements ClickListener {
		
		private Transformation transformation;
		
		public EditTransformationButton(Transformation transformation) {
			this.transformation = transformation;
			
			setCaption("edit");
			setStyleName("link");
			
			addClickListener(this);
		}
		
		@Override
		public void buttonClick(ClickEvent event) {
			final Window window = new Window(String.format("Edit Transformation '%s'", transformation.getName()));
			window.setModal(true);
			window.setDraggable(false);
			window.setResizable(false);
			window.addCloseListener(new CloseListener() {
				@Override
				public void windowClose(CloseEvent e) {
					refreshTable();
				}
			});
			
			TransformationEditor editor = new TransformationEditor(transformation, datasinkService.getDatasinks(), datasourceService.getDatasources());
			editor.addSaveListener(new SaveListener() {
				@Override
				public void save(SaveEvent event) {
					if (event.getTransformation() != null) {
						transformationService.updateTransformation(event.getTransformation());
					}
					window.close();
				}
			});
			window.setContent(editor);
			
			UI.getCurrent().addWindow(window);
		}
		
	}
	
	private class DeleteTransformationButton extends Button implements ClickListener {
		
		private Transformation transformation;
		
		public DeleteTransformationButton(Transformation transformation) {
			this.transformation = transformation;
			
			setCaption("delete");
			setStyleName("link");
			
			addClickListener(this);
		}
		
		@Override
		public void buttonClick(ClickEvent event) {
			transformationService.removeTransformation(transformation);
			refreshTable();
		}
		
	}

	private class NewTransformationHandler implements ClickListener {
		
		@Override
		public void buttonClick(ClickEvent event) {
			final Window window = new Window("New Transformation");
			window.setModal(true);
			window.setDraggable(false);
			window.setResizable(false);
			window.addCloseListener(new CloseListener() {
				@Override
				public void windowClose(CloseEvent e) {
					refreshTable();
				}
			});
			
			TransformationEditor editor = new TransformationEditor(new Transformation(), datasinkService.getDatasinks(), datasourceService.getDatasources());
			editor.addSaveListener(new SaveListener() {
				@Override
				public void save(SaveEvent event) {
					if (event.getTransformation() != null) {
						transformationService.addTransformation(event.getTransformation());
					}
					window.close();
				}
			});
			window.setContent(editor);
			
			UI.getCurrent().addWindow(window);
		}
		
	}

	@AutoGenerated
	private VerticalLayout buildMainLayout() {
		// common part: create layout
		mainLayout = new VerticalLayout();
		mainLayout.setImmediate(false);
		mainLayout.setWidth("100%");
		mainLayout.setHeight("100%");
		mainLayout.setMargin(true);
		mainLayout.setSpacing(true);
		
		// top-level component properties
		setWidth("100.0%");
		setHeight("100.0%");
		
		// label
		label = new Label();
		label.setStyleName("h1");
		label.setImmediate(false);
		label.setWidth("-1px");
		label.setHeight("-1px");
		label.setValue("Transformations");
		mainLayout.addComponent(label);
		
		// table
		table = new Table();
		table.setImmediate(false);
		table.setWidth("100.0%");
		table.setHeight("100.0%");
		mainLayout.addComponent(table);
		mainLayout.setExpandRatio(table, 1.0f);
		
		// buttons
		buttons = buildButtons();
		mainLayout.addComponent(buttons);
		
		return mainLayout;
	}

	@AutoGenerated
	private HorizontalLayout buildButtons() {
		// common part: create layout
		buttons = new HorizontalLayout();
		buttons.setImmediate(false);
		buttons.setWidth("-1px");
		buttons.setHeight("-1px");
		buttons.setMargin(false);
		buttons.setSpacing(true);
		
		// newTransformationButton
		newTransformationButton = new Button();
		newTransformationButton.setStyleName("small");
		newTransformationButton.setCaption("New Transformation...");
		newTransformationButton.setImmediate(false);
		newTransformationButton.setWidth("-1px");
		newTransformationButton.setHeight("-1px");
		buttons.addComponent(newTransformationButton);
		
		return buttons;
	}

}
